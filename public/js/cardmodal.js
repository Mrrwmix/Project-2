$(document).ready(function() {
  $("title").text(`${$(".cat").text()}`);
  $(".modal").modal();
  $(document).on("click", ".card", function() {
    $("#modal-header").text(
      $(this)
        .children(".card-content")
        .children(".header")
        .text()
    );
    $("#title_author").text(
      $(this)
        .children(".card-content")
        .children(".meta")
        .text()
    );
    $("#modal-img").attr(
      "src",
      $(this)
        .children(".center-align")
        .children("img")
        .attr("src")
    );
    $("#review").text(
      $(this)
        .children(".card-content")
        .children("p")
        .text()
    );
    $("#score").text(
      "★".repeat(
        parseInt(
          $(this)
            .children(".card-content")
            .children(".description")
            .text()
        )
      ) +
        "☆".repeat(
          5 -
            parseInt(
              $(this)
                .children(".card-content")
                .children(".description")
                .text()
            )
        )
    );
  });
});
